void swaps(char *x, char *y)
{
    char temp;
    temp = *x;
    *x = *y;
    *y = temp;
}

void swapi(int *a, int *b)
{
    int temp;
    temp = *a;
    *a = *b;
    *b = temp;
}

long long value(vector<long long> a, long long i)
{
    return i >= a.size() ? 0 : a[i];
}

void compute()
{
    long long n, ans = 0;
    cin >> n;
    vector<long long> a(n), s(n);
    for (long long &x : a)
        cin >> x;

    for (long long i = n - 1; i >= 0; --i)
    {
        s[i] = a[i] + ((i + a[i] >= n) ? 0 : s[i + a[i]]);
        ans = max(ans, s[i]);
    }
    cout << ans << "\n";
}

int main()
{
#ifdef debug
    freopen("input.txt", "r", stdin);
    freopen("output.txt", "w", stdout);
    freopen("log.txt", "w", stderr);
#endif
    int t;
    scanf("%d", &t);
    while (t--)
    {
        compute();
    }
#ifdef debug
    fprintf(stdout, "\nTIME: %.3lf sec\n", (double)clock() / (CLOCKS_PER_SEC));
#endif
    return 0;
}