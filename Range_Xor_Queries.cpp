void swaps(char *x, char *y)
{
    char temp;
    temp = *x;
    *x = *y;
    *y = temp;
}

void swapi(int *a, int *b)
{
    int temp;
    temp = *a;
    *a = *b;
    *b = temp;
}

void compute()
{
    long long n, q, a, b, x, ans;
    cin >> n >> q;
    vector<long long> p(n + 1);

    for (int i = 1; i <= n; ++i)
    {
        cin >> x;
        p[i] = p[i - 1] ^ x;
    }

    for (long long j = 0; j < q; ++j)
    {
        cin >> a >> b;
        ans = p[b] ^ p[a - 1];
        cout << ans << "\n";
    }
}

