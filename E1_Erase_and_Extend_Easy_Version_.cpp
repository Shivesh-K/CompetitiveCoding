void swaps(char *x, char *y)
{
    char temp;
    temp = *x;
    *x = *y;
    *y = temp;
}

void swapi(int *a, int *b)
{
    int temp;
    temp = *a;
    *a = *b;
    *b = temp;
}

void compute()
{
    int n, k;
    string s;
    cin >> n >> k >> s;

    int repeat = 1;
    for (int i = 1; i < n; ++i)
    {
        if (s[i] < s[i % repeat])
            repeat = i + 1;
        else if (s[i] > s[i % repeat])
            break;
    }

    int i = 0;
    while (k--)
    {
        cout << s[i++];
        if (i >= repeat)
            i = 0;
    }
    cout << "\n";
}

