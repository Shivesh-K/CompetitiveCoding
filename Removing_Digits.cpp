void swaps(char *x, char *y)
{
    char temp;
    temp = *x;
    *x = *y;
    *y = temp;
}

void swapi(int *a, int *b)
{
    int temp;
    temp = *a;
    *a = *b;
    *b = temp;
}

void compute()
{
    int n, ans = 0, t, mx;
    cin >> n;

    while (n > 0)
    {
        ++ans;
        t = n;
        mx = 0;
        while (t)
        {
            if (t % 10 > mx)
                mx = t % 10;
            t /= 10;
        }
        n -= mx;
    }
    cout << ans << "\n";
}

