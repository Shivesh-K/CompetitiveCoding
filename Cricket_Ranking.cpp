void compute()
{
    int r1, c1, w1, r2, c2, w2;
    cin >> r1 >> c1 >> w1 >> r2 >> c2 >> w2;
    int x = 0;
    r1 > r2 ? ++x : --x;
    c1 > c2 ? ++x : --x;
    w1 > w2 ? ++x : --x;

    if (x > 0)
        cout << "A\n";
    else
        cout << "B\n";
}

int main()
{
#ifdef debug
    freopen("input.txt", "r", stdin);
    freopen("output.txt", "w", stdout);
    freopen("log.txt", "w", stderr);
#endif
    int t;
    scanf("%d", &t);
    while (t--)
    {
        compute();
    }
#ifdef debug
    fprintf(stdout, "\nTIME: %.3lf sec\n", (double)clock() / (CLOCKS_PER_SEC));
#endif
    return 0;
}